const os = require('os');

console.log(os.version()); // prints the OS version of the current system

console.log(os.userInfo()); // prints the userInfo such as username and home directory

console.log(os.uptime()); // prints the system uptime

console.log(os.freemem()); // prints the free memory(RAM) in the system

console.log(os.totalmem()); // prints the total memory(RAM) in the system